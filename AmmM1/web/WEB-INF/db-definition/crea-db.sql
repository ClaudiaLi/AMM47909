CREATE TABLE Saldo(
   id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   saldo DOUBLE PRECISION
);

CREATE TABLE Cliente(
   id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   nome VARCHAR(40),
   cognome VARCHAR(40),
   username VARCHAR(40),
   password VARCHAR(15),
   idSaldo INTEGER,
   FOREIGN KEY (idSaldo) REFERENCES Saldo(id)
);

CREATE TABLE Venditore(
   id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   nome VARCHAR(40),
   cognome VARCHAR(40),
   username VARCHAR(40),
   password VARCHAR(15),
   idSaldo INTEGER,
   FOREIGN KEY (idSaldo) REFERENCES Saldo(id)
);

CREATE TABLE Articolo(
   id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   nomeArticolo VARCHAR(40),
   urlImmagine VARCHAR(40),
   descrizione VARCHAR(200),
   prezzo DOUBLE PRECISION,
   quantita INTEGER,
   idVenditore INTEGER,
   FOREIGN KEY (idVenditore) REFERENCES Venditore(id)
);


